    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DecimalFormat df = new DecimalFormat(".##");
        try {
            String altura[] = alturas.getText().split(",");
            double vmenor = Double.parseDouble(altura[0]);

            for (int i = 0; i < altura.length; i++) {
                if (Double.parseDouble(altura[i]) < vmenor) {
                    vmenor = Double.parseDouble(altura[i]);
                }
            }

            menor.setText("" + df.format(vmenor));
        } catch (Exception e) {
             menor.setText("Formato Inválido");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

