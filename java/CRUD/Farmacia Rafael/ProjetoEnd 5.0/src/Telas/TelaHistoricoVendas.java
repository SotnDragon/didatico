    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        try {
            con.executaSql("select * from historico");

            int i = 0;

            while (con.rs.next()) {

                String data = con.rs.getString("data");
                String valor = con.rs.getString("valor_venda");
                String vendedor = con.rs.getString("vendedor");

                tabela.setValueAt(data, i, 0);
                tabela.setValueAt(valor, i, 1);
                tabela.setValueAt(vendedor, i, 2);
        
                i++;
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Nenhum Produto Cadastrado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

